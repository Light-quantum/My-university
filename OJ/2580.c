/********************************************************
    题目描述
    Description
    有两个整数，分别为n和k。
    请你计算出由数字 1,3,5,7和0 组成的n位数中
    数字各位的和为k的共有多少个。（数字可重复使用）
    输入描述
    Input
    每行为一组，有两个整数用空格隔开，
    分别代表 n(0<n≤11) 和 k(0<k≤77)
    以EOF结束
    输出描述
    Output
    输出描述：
    每一行只有一个整数，对应一组结果。
********************************************************/