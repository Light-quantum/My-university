/********************************************************************************
    题目描述
    Description
    有N个学生，每个学生的数据包括学号、姓名、3门课的成绩，从键盘输入N个学生的数据，
    要求打印出3门课的总平均成绩，以及总分最高的学生的数据（包括学号、姓名、3门课成绩）
    输入描述
    Input
    学生数量N占一行,接下来的N行输入每个学生的学号、姓名、三科成绩，空格分开。
    输出描述
    Output
    3门课的总平均成绩，以及总分最高的学生的数据（包括学号、姓名、3门课成绩）
******************************************************************************/
